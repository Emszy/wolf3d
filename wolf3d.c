#define sign(x) ((x>0)?1:((x<0)?-1:0))
     void draw(t_connection *obj, int x1,int y1,int x2,int y2, int color)
{
int x,y,dx,dy,swap,temp,s1,s2,p,i;

x=x1;
y=y1;
dx=abs(x2-x1);
dy=abs(y2-y1);
s1=sign(x2-x1);
s2=sign(y2-y1);
swap=0;
  mlx_pixel_put(obj->mlx, obj->win, x, y, color);
if(dy>dx)
 {
 temp=dx;
 dx=dy;
 dy=temp;
 swap=1;
 }
p=2*dy-dx;
for(i=0;i<dx;i++)
 {
  mlx_pixel_put(obj->mlx, obj->win, x, y, color);
 while(p>=0)
  {
  p=p-2*dx;
  if(swap)
   x+=s1;
  else
   y+=s2;
  }
 p=p+2*dy;
 if(swap)
  y+=s2;
 else
  x+=s1;
 }
  mlx_pixel_put(obj->mlx, obj->win, x, y, color);
}
   
          
          
      
int main(void)
{

  t_connection *obj;

  obj = malloc(sizeof(t_connection));
  initializer(obj);



  double posX;
  double posY;      //x and y start position
  double dirX;
  double dirY;     //initial direction vector
  double planeX;
  double planeY; //the 2d raycaster version of camera plane
  double time; //time of current frame
  double oldTime;  //time of previous frame
  int x;
  int done;
  int color;


  done = 0;
  posX = 20;
  posY = 10;
  dirX = -1;
  dirY = 0;     
  planeX = 0;
  planeY = 0.66;
  time = 0;     
  oldTime = 0;
  color = 0;

  initializer(obj);

    x = -1;
    while(++x < WIDTH)
    {
      //calculate ray position and direction
      double cameraX = 2 * x / (double)WIDTH - 1; //x-coordinate in camera space
      double rayPosX = posX;
      double rayPosY = posY;
      double rayDirX = dirX + planeX * cameraX;
      double rayDirY = dirY + planeY * cameraX;
      //which box of the map we're in
      int mapX = (int)rayPosX;
      int mapY = (int)rayPosY;
      //length of ray from current position to next x or y-side
      double sideDistX;
      double sideDistY;

       //length of ray from one x or y-side to next x or y-side
      double deltaDistX = sqrt(1 + (rayDirY * rayDirY) / (rayDirX * rayDirX));
      double deltaDistY = sqrt(1 + (rayDirX * rayDirX) / (rayDirY * rayDirY));
      double perpWallDist;

      //what direction to step in x or y-direction (either +1 or -1)
      int stepX;
      int stepY;

      int hit = 0; //was there a wall hit?
      int side; //was a NS or a EW wall hit?
      //calculate step and initial sideDist



      //calculate step and initial sideDist
      if (rayDirX < 0)
      {
        stepX = -1;
        sideDistX = (rayPosX - mapX) * deltaDistX;
      }
      else
      {
        stepX = 1;
        sideDistX = (mapX + 1.0 - rayPosX) * deltaDistX;
      }
      if (rayDirY < 0)
      {
        stepY = -1;
        sideDistY = (rayPosY - mapY) * deltaDistY;
      }
      else
      {
        stepY = 1;
        sideDistY = (mapY + 1.0 - rayPosY) * deltaDistY;
      }
      //perform DDA
      while (hit == 0)
      {
        //jump to next map square, OR in x-direction, OR in y-direction
        if (sideDistX < sideDistY)
        {
          sideDistX += deltaDistX;
          mapX += stepX;
          side = 0;
        }
        else
        {
          sideDistY += deltaDistY;
          mapY += stepY;
          side = 1;
        }
        //Check if ray has hit a wall
        if (worldMap[mapX][mapY] > 0)
          hit = 1;
      }


      //Calculate distance projected on camera direction (oblique distance will give fisheye effect!)
      if (side == 0) 
        perpWallDist = (mapX - rayPosX + (1 - stepX) / 2) / rayDirX;
      else
        perpWallDist = (mapY - rayPosY + (1 - stepY) / 2) / rayDirY;

      //Calculate height of line to draw on screen
      int lineHeight = (int)(HEIGHT / perpWallDist);

      //calculate lowest and highest pixel to fill in current stripe
      int drawStart = -lineHeight / 2 + HEIGHT / 2;
      if(drawStart < 0)
        drawStart = 0;
      int drawEnd = lineHeight / 2 + HEIGHT / 2;
      if(drawEnd >= HEIGHT)
        drawEnd = HEIGHT - 1;

      //choose wall color
      if (worldMap[mapX][mapY] == 1)
        color = RED;
      else if (worldMap[mapX][mapY] == 2)
          color = GREEN;
      else if (worldMap[mapX][mapY] == 3)
          color = BLUE;
      else if (worldMap[mapX][mapY] == 4)
          color = WHITE;
      else
          color = YELLOW;

       if (side == 1 && color == RED) 
        color = SHADOW_RED;
       if (side == 1 && color == GREEN) 
        color = SHADOW_GREEN;
       if (side == 1 && color == BLUE) 
        color = SHADOW_BLUE;
       if (side == 1 && color == GREEN) 
        color = SHADOW_GREEN;
       if (side == 1 && color == YELLOW) 
        color = SHADOW_YELLOW;
      draw(obj, x, drawStart, x, drawEnd, color);
    }

 mlx_loop(obj->mlx);


}








